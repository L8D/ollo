#!/usr/bin/env bash
set -euo pipefail

# в”Ђв”Ђв”Ђ Usage в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# ollo emit-hook-event <HOOK_TYPE>
#
# Generic hook event emitter. Reads Claude Code hook JSON from stdin,
# extracts relevant fields, and appends a ClaudeHookFired event.
#
# HOOK_TYPE: SessionStart | Stop | PermissionRequest | PreToolUse | UserPromptSubmit

HOOK_TYPE="${1:?Usage: ollo emit-hook-event <HOOK_TYPE>}"

# Exit silently if no ticket context
if [[ -z "${KOTA_CURRENT_TICKET_ID:-}" ]]; then
  exit 0
fi

# в”Ђв”Ђв”Ђ Read stdin в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
INPUT=$(cat)

# в”Ђв”Ђв”Ђ Extract common fields в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
SESSION_ID=$(echo "$INPUT" | jq -r '.session_id // ""')
CWD=$(echo "$INPUT" | jq -r '.cwd // ""')

# в”Ђв”Ђв”Ђ Build emit args в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
EMIT_ARGS=(
  "$KOTA_CURRENT_TICKET_ID"
  ClaudeHookFired
  --origin=claude-hook
  "hook=$HOOK_TYPE"
  "sessionId=$SESSION_ID"
  "cwd=$CWD"
)

# в”Ђв”Ђв”Ђ Extract hook-type-specific fields в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
case "$HOOK_TYPE" in
  SessionStart)
    SOURCE=$(echo "$INPUT" | jq -r '.source // ""')
    EMIT_ARGS+=("source=$SOURCE")
    ;;
  PreToolUse)
    TOOL_NAME=$(echo "$INPUT" | jq -r '.tool_name // ""')
    EMIT_ARGS+=("tool=$TOOL_NAME")
    ;;
  UserPromptSubmit)
    PROJECT_KEY="$(echo "$CWD" | tr '/' '-' | tr '.' '-')"
    TRANSCRIPT_PATH="$HOME/.claude/projects/${PROJECT_KEY}/${SESSION_ID}.jsonl"
    EMIT_ARGS+=("transcriptPath=$TRANSCRIPT_PATH")
    ;;
esac

# в”Ђв”Ђв”Ђ Emit в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
ollo emit "${EMIT_ARGS[@]}"
